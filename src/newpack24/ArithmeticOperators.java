package newpack24;

public class ArithmeticOperators {

	public static void main(String[] args) {
	int a=20;
	int b=10;
	int c=a+b;
	System.out.println(c);
	int d=a-b;
	System.out.println(d);
	int e=a*b;
	System.out.println(e);
	int f=a/b;
	System.out.println(f);
	int g=a%b;
	System.out.println(g);

	}

}
